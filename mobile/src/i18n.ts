export type Translations = typeof translations.pl;

/** React hook — use inside components */
export function useT(): Translations {
  const lang = useStore((s) => s.language);
  return translations[lang] as unknown as Translations;
}

/** Non-hook getter — use in callbacks, Alert handlers, etc. */
export function getT(): Translations {
  const lang = useStore.getState().language;
  return translations[lang] as unknown as Translations;
}
